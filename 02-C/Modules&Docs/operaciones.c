#include "operaciones.h"

// * Implementación de la función sumar
/*
 * Suma dos números enteros.
 * @param a El primer número entero
 * @param b El segundo número entero
 * @return El resultado de la suma
 */
int sumar(int a, int b)
{
    return a + b;
}
// * Implementación de la función restar
int restar(int a, int b)
{
    return a - b; // * Retorna el resultado de restar b a a
}
// * Implementación de la función multiplicar
int multiplicar(int a, int b)
{
    return a * b; // * Retorna el resultado de multiplicar a y b
}